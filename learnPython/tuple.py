#元组
#元祖与列表相似，不同之处在于元组的元素不能修改，元组写在小括号（）里，元素之间用逗号隔开。
#元组中的元素类型也可以不相同
tuple = ('abcd',782,2.23,'runoob',70.2)
tinytuple = (123,'runoob')
print(tuple)#('abcd',782,2.23,'runoob',70.2)
print(type(tuple[0]))#str
print(tuple[0])#abcd
print(tuple[1:3])#(782,2.23)
print(tuple[2:])#(2.23,'runoob',70.2)
print(tinytuple*2)#(123,'runoob',123,'runoob')
print(tuple+tinytuple)#('abcd',782,2.23,'runoob',70.2,123,'runoob')
tuple[2] = 123; #错误

