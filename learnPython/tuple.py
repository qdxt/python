#元组与字符串类似，可以被索引且下标索引从0开始，-1从末尾开始的位置，也可以进行截取
#注意⚠️ 可以把字符串看作一种特殊的元组
tup = (1,2,3,4,5,6)
print(tup[0])#1
print(tup[1:5])#(2,3,4,5)
#tup[0] =11 修改元素元组的操作是非法的

